package User_Input;
import java.util.Scanner;
public class Input {

        public static void main(String[] args) {
            Scanner sc = new Scanner(System.in);
            System.out.println("Enter a number:-  ");
            int num = sc.nextInt();
            System.out.println(num);

        }

//Import Statement: import java.util.Scanner; imports the Scanner class from the java.util package.
//Creating a Scanner Object: Scanner scanner = new Scanner(System.in);
// initializes a Scanner object to read input from the standard input stream (keyboard).
}
